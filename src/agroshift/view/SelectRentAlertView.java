    private void formatoTabla(){

        int[] weights = {35,10,10,35,10};
            
        for(int i = 0; i < tblRentasSinEmpezar.getColumnCount(); i++){
            tblRentasSinEmpezar.getColumnModel().getColumn(i).setPreferredWidth(weights[i]);
        }
        
        DefaultTableCellRenderer headerRenderer = new DefaultTableCellRenderer();
        headerRenderer.setBackground(new Color(0, 139, 139));
        headerRenderer.setForeground(Color.BLACK);
        headerRenderer.setFont(new Font("Segoe UI",Font.BOLD,14));
        headerRenderer.setOpaque(true);
        headerRenderer.setForeground(Color.BLACK);
  
        for (int i = 0; i < tblRentasSinEmpezar.getModel().getColumnCount(); i++) {          //Recorro y se lo aplico a cada header
            tblRentasSinEmpezar.getColumnModel().getColumn(i).setHeaderRenderer(headerRenderer);    
        }
    }
    
    private void cargarRentasTabla(){
        
        int numberColumns = 5;
        try{
            DefaultTableModel model = new DefaultTableModel(){
                @Override
                public boolean isCellEditable(int i, int i1){
                    return false;
                }
            };
            tblRentasSinEmpezar.setModel(model);
            model.addColumn("Cliente");
            model.addColumn("Inicio");
            model.addColumn("Fin");
            model.addColumn("Responsable");
            model.addColumn("Cant. equipos");
            
            formatoTabla();
            
            rentas_sin_comenzar = RentController.obtenerRentasTerminadas();
            
            for(Rent rent: rentas_sin_comenzar){
                Object[] rows = new Object[numberColumns];
                rows[0] = ClientController.obtenerClientePorId(rent.getId_cliente()).getNombre();
                rows[1] = rent.getFecha_inicio();
                rows[2] = rent.getFecha_fin();
                rows[3] = EmployeeController.obtenerEmpleadoPorId(rent.getId_empleado()).getNombre();
                rows[4] = rent.getEquipos().size();
                model.addRow(rows);
            }
                
        } catch(Exception e){
            System.out.println(e.getMessage());
            JOptionPane.showMessageDialog(null,"Error al cargar las rentas");
        }
    }
    
