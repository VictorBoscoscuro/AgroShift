     private void formatoTabla(){
                
        int[] weights = {75,25};
            
        for(int i = 0; i < tblAlertasNoEnCurso.getColumnCount(); i++){
            tblAlertasNoEnCurso.getColumnModel().getColumn(i).setPreferredWidth(weights[i]);
        }
        
        DefaultTableCellRenderer headerRenderer = new DefaultTableCellRenderer();
        headerRenderer.setBackground(new Color(30, 30, 30));
        headerRenderer.setForeground(Color.WHITE);
        headerRenderer.setFont(new Font("Segoe UI",Font.BOLD,14));
        headerRenderer.setOpaque(true);
        headerRenderer.setHorizontalAlignment(SwingConstants.CENTER);
  
        for (int i = 0; i < tblAlertasNoEnCurso.getModel().getColumnCount(); i++) {          //Recorro y se lo aplico a cada header
            tblAlertasNoEnCurso.getColumnModel().getColumn(i).setHeaderRenderer(headerRenderer);    
        }
        
        DefaultTableCellRenderer cellRenderer = new DefaultTableCellRenderer();
        cellRenderer.setBackground(new Color(240, 240, 240));
        cellRenderer.setForeground(Color.BLACK);
        cellRenderer.setFont(new Font("Segoe UI",Font.PLAIN,13));
        cellRenderer.setOpaque(true);
        cellRenderer.setHorizontalAlignment(SwingConstants.CENTER);
        
        for (int i = 0; i < tblAlertasNoEnCurso.getColumnCount(); i++) {          //Recorro y se lo aplico a cada header
            tblAlertasNoEnCurso.getColumnModel().getColumn(i).setCellRenderer(cellRenderer);    
        }
    }
    
    private void cargarAlertasNoEnCurso(){
        alertasNoEnCurso = AlertController.obtenerAlertasNoEnCurso();
        int numberColumns = 2;
        try{
            DefaultTableModel model = new DefaultTableModel(){
                @Override
                public boolean isCellEditable(int i, int i1){
                    return false;
                }
            };
            tblAlertasNoEnCurso.setModel(model);
            model.addColumn("Alerta");
            model.addColumn("Inicio");
            
            formatoTabla();
            
            for(Alerta alerta: alertasNoEnCurso){
                Object[] rows = new Object[numberColumns];
                rows[0] = alerta.getNombre();
                rows[1] = alerta.getFecha_inicio_en_curso();
                model.addRow(rows);
            }
                
        } catch(Exception e){
            System.out.println(e.getMessage());
            JOptionPane.showMessageDialog(null,"Error al cargar las alertas");
        }
    }
    
