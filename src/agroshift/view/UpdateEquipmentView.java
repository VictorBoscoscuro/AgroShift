    private boolean fechaMenorIgualHoy(String dia, String mes, String anio){
        try{
            DateTimeFormatter formatter = DateTimeFormatter.ofPattern("yyyy-MM-dd");
        
            String date = anio+"-"+mes+"-"+dia;
            LocalDate parsedDate = LocalDate.parse(date,formatter);
            if(parsedDate.isAfter(LocalDate.now())){            //SI LA FECHA ES POSTERIOR A HOY (OSEA QUE NO CORRESPONDE)
                return false;
            } else return true;
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Error al parsear la fecha");
            return false;
        }
    }
    
    private boolean validarFecha(){
        try{
            if(Integer.parseInt(txtDiaAdq.getText()) > 0 && Integer.parseInt(txtDiaAdq.getText()) < 32){
                if(Integer.parseInt(txtMesAdq.getText()) > 0 && Integer.parseInt(txtMesAdq.getText()) < 13){
                    if(Integer.parseInt(txtAnioAdq.getText()) > 2010 && Integer.parseInt(txtAnioAdq.getText()) < 2100){
                        if(fechaMenorIgualHoy(txtDiaAdq.getText(),txtMesAdq.getText(),txtAnioAdq.getText())){
                            return true;
                        } else{
                            JOptionPane.showMessageDialog(null, "La fecha no puede superior a la actual");
                            return false;
                        }
                    } else return false;
                } else return false;
            } else return false;
        }catch(Exception e){
            return false;
        }
    }
    
    private boolean validarCampos(){
        if(txtMarca.getText().length() > 0){
            if(txtCodigo.getText().length() > 3 && txtCodigo.getText().length() < 9){
                if(validarFecha()){
                    return true;
                } else{
                    JOptionPane.showMessageDialog(null, "Error en la fecha ingresada");
                    return false;
                }
            } else{
                JOptionPane.showMessageDialog(null, "El codigo debe contener entre 4 y 8 caracteres");
                txtCodigo.requestFocus();
                return false;
            }
        } else{
            JOptionPane.showMessageDialog(null, "Ingrese la marca");
            txtMarca.requestFocus();
            return false;
        }
    }
    
    private void btnNewMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnNewMouseClicked
        if(validarCampos()){
            try{
                if(EquipmentController.actualizarEquipo(equipoSeleccionado)){
                    EquipmentView form = new EquipmentView();
                    form.setVisible(true);
                    this.dispose();
                } else{
                    JOptionPane.showMessageDialog(null, "Error al actualizar el equipo");
                }
            } catch(Exception e){
                JOptionPane.showMessageDialog(null, "Error al actualizar el equipo. "+e.getMessage());
            }
        }
    }//GEN-LAST:event_btnNewMouseClicked

