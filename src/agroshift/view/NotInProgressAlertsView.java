    Alerta alerta_seleccionada;
    
    private void tblAlertasNoEnCursoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblAlertasNoEnCursoMouseClicked
        int FILA = tblAlertasNoEnCurso.getSelectedRow();
        if(FILA != -1){
            alerta_seleccionada = alertasNoEnCurso.get(FILA);
            txtDescripcion.setText(alerta_seleccionada.getDescripcion());
            txtNombre.setText(alerta_seleccionada.getNombre());
            txtInicio.setText(alerta_seleccionada.getFecha_inicio_en_curso());
            txtVencimiento.setText(alerta_seleccionada.getFecha_fin());
        }
    }//GEN-LAST:event_tblAlertasNoEnCursoMouseClicked

    //IMPLEMENTAR
    private boolean validarCampos(){
        return true;
    }
    private void setearCampos(){
        alerta_seleccionada.setDescripcion(txtDescripcion.getText().toUpperCase());
        alerta_seleccionada.setNombre(txtNombre.getText().toUpperCase());
        alerta_seleccionada.setFecha_fin(txtVencimiento.getText());
        alerta_seleccionada.setFecha_inicio_en_curso(txtInicio.getText());
    }
    
    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        if(validarCampos()){
            setearCampos();
            if(AlertController.actualizarAlerta(alerta_seleccionada)){
                NotInProgressAlertsView form = new NotInProgressAlertsView();
                form.setVisible(true);
                this.dispose();
            }
        }
    }//GEN-LAST:event_btnActualizarActionPerformed

    private void btnDescartarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDescartarActionPerformed
        int FILA = tblAlertasNoEnCurso.getSelectedRow();
        if(FILA != -1){
            if(AlertController.eliminarAlerta(alertasNoEnCurso.get(FILA).getId_alerta())){
                JOptionPane.showMessageDialog(null, "Alerta descartada");
                cargarAlertasNoEnCurso();
            }
        }
    }//GEN-LAST:event_btnDescartarActionPerformed

