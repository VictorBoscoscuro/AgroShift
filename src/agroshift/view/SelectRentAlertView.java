    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int FILA = tblRentasSinEmpezar.getSelectedRow();
        if(FILA != -1){
            Alerta alerta = new Alerta();
            alerta.setNombre("INICIO DE RENTA A CLIENTE: "+ClientController.obtenerClientePorId(rentas_sin_comenzar.get(FILA).getId_cliente()).getCodigo()+" EN LA FECHA "+rentas_sin_comenzar.get(FILA).getFecha_inicio());
            alerta.setDescripcion("EMPLEADO RESPONSABLE: "+EmployeeController.obtenerEmpleadoPorId(rentas_sin_comenzar.get(FILA).getId_empleado()).getNombre());
            alerta.setFecha_creacion(LocalDate.now().toString());
            alerta.setFecha_inicio_en_curso(txtInicio.getText());
            alerta.setFecha_fin(txtfin.getText());
            AlertController.crearAlerta(alerta);
            JOptionPane.showMessageDialog(null, "Alerta creada");
        } else{
            JOptionPane.showMessageDialog(null, "Seleccione primero la renta");
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

