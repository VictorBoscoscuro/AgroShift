     private void formatoTabla(){
                
        int[] weights = {235,150,205,225};
            
        for(int i = 0; i < tblClients.getColumnCount(); i++){
            tblClients.getColumnModel().getColumn(i).setPreferredWidth(weights[i]);
        }
        
        DefaultTableCellRenderer headerRenderer = new DefaultTableCellRenderer();
        headerRenderer.setBackground(new Color(0, 139, 139));
        headerRenderer.setForeground(Color.BLACK);
        headerRenderer.setFont(new Font("Segoe UI",Font.BOLD,14));
        headerRenderer.setOpaque(true);
        headerRenderer.setForeground(Color.BLACK);
  
        for (int i = 0; i < tblClients.getModel().getColumnCount(); i++) {          //Recorro y se lo aplico a cada header
            tblClients.getColumnModel().getColumn(i).setHeaderRenderer(headerRenderer);    
        }
    }
    
